def view_param_dialog(choice_1_1: str) -> tuple:
    find = ""
    choice_view = ""
    match choice_1_1:

        case "1":
            print("Все книги Каталога:")
            find = ""
            choice_view = 0

        case "2":
            find = input("Введите искомое название - часть или полностью:\n... ")
            choice_view = 0

        case "3":
            find = input("Введите искомого автора - часть или полностью:\n... ")
            choice_view = 1

        case "4":
            find = input("Введите искомый год издательства (пример для ввода: 1987):\n... ")
            choice_view = 2

        case "5":
            find = input("Введите искомый жанр (Роман, Приключение, Фантастика):\n... ")
            choice_view = 3

        case "6":
            find = input("Введите количество экземпляров, по которому нужно отыскать книги:\n... ")
            choice_view = 4
    return choice_view,find

