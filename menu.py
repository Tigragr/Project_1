    while True:
        key_edi_del = input(f"Ведите Ключ той записи Каталога, которую требуется {'изменить' if var == 'edit' else 'удалить'}\n"
                        "... ")
        if key_edi_del.isdigit() != True:
            print("!!! Необходимо ввести одно целочисленное значение !!!")
            continue
        break

    key_edi_del = int(key_edi_del)

    return key_edi_del




